NN_neighbours = find(ig2_mag>=GCUT^2,1)-1;

icount=0;
for i=1:NN_neighbours
    ig2=ig2_mag(i);
    a=sqrt(double(ig2));
    ia=floor(a);
    ic=0;
    for igx=-ia:ia
        igx2=igx*igx;
        for jgy=-ia:ia
            jgy2=jgy*jgy;
            igxy2=igx2+jgy2;
            for kgz=-ia:ia
                kgz2=kgz*kgz;
                igxyz2=igxy2+kgz2;
                if (igxyz2==ig2)
                    ic=ic+1;
                    gx(ic+icount)=igx;
                    gy(ic+icount)=jgy;
                    gz(ic+icount)=kgz;
                end
            end
        end
    end
    %call gen_gvecs(i,ia,ig2,ic)
    icount=icount+ic;
    % fprintf('%d %d %d\n',ig2,ic,icount);
end

vec_g = [gx(:),gy(:),gz(:)];

if nargout>1
    igx = (gy+gz)/2;
    igy = (gx+gz)/2;
    igz = (gx+gy)/2;
    vec_ig = [igx(:),igy(:),igz(:)];
end
